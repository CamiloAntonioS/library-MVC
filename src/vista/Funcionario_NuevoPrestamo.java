    private void btn_validarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_validarUsuarioActionPerformed
        try {
            String usuarioRut = this.txt_rut_usuario.getText();
            if (ValidacionRut.IngresoRut(usuarioRut)) {
                this.usuarioaPrestar = new Usuario(usuarioRut);
                this.libroaPrestar = new Libro();
                if (this.usuarioaPrestar.obtenerDatosPrestamo()) {
                    this.fprestamo_filtro_nombre.setEnabled(true);
                    this.fprestamo_submit.setEnabled(true);
                    this.tbl_textos_prestamo.setEnabled(true);
                    this.listarlibros();
                } else {
                    JOptionPane.showMessageDialog(null, "Rut no existe en Biblioteca!", "Rut mal ingresado!", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Rut ingresado no es valido!", "Rut mal ingresado!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_validarUsuarioActionPerformed

