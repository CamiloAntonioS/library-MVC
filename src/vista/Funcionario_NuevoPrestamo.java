    private void tbl_textos_prestamoMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_textos_prestamoMouseReleased
        int selectedRow = this.tbl_textos_prestamo.getSelectedRow();
        if (selectedRow > -1) {
            String demandaLibro = (String) this.tbl_textos_prestamo.getValueAt(selectedRow, 7);
            int idLibro = (Integer) this.tbl_textos_prestamo.getValueAt(selectedRow, 0);
            this.libroaPrestar.setId(idLibro);
            String advertencia;
            switch (demandaLibro) {
                case "Alta":
                    advertencia = (this.usuarioaPrestar.getPerfil() == 3) ? "3 dias." : "2 dias.";
                    this.fprestamo_label_advertencia.setText("Advertencia: El texto solicitado es de demanda Alta. La duración del prestamo será de " + advertencia);
                    this.fprestamo_label_advertencia.setForeground(Color.RED);
                    this.libroaPrestar.setDemanda("Alta");
                    break;
                case "Normal":
                    advertencia = (this.usuarioaPrestar.getPerfil() == 3) ? "10 dias." : "5 dias.";
                    this.fprestamo_label_advertencia.setText("Advertencia: El texto solicitado es de demanda Alta. La duración del prestamo será de " + advertencia);
                    this.fprestamo_label_advertencia.setForeground(Color.BLACK);
                    this.libroaPrestar.setDemanda("Normal");
                    break;
            }
        }
    }//GEN-LAST:event_tbl_textos_prestamoMouseReleased

    private void fprestamo_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fprestamo_submitActionPerformed
        try {
            this.prestamoNuevo.setPrestamo_usuario(this.usuarioaPrestar.getId());
            this.prestamoNuevo.setPerfil_usuario(this.usuarioaPrestar.getPerfil());
            this.prestamoNuevo.setPrestamo_libro(this.libroaPrestar.getId());
            this.prestamoNuevo.setPrestamo_sede(this.funcionario.getSede());
            this.prestamoNuevo.setDemanda_libro(this.libroaPrestar.getDemanda());
            this.prestamoNuevo.setPrestamo_funcionario(this.funcionario.getId());
            this.prestamoNuevo.registrarPrestamo();
        } catch (Exception ex) {
            Logger.getLogger(Funcionario_NuevoPrestamo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_fprestamo_submitActionPerformed

    private void listarlibros() throws SQLException {
        Libro libro = new Libro();
        ResultSet rs = libro.getTodoLibrosTabla();
        this.llenarTabla(rs);
    }

    private void filtrarlibros(String filtro_nombre) throws SQLException, Exception {
        Libro libro = new Libro();
        ResultSet rs = libro.filtrarLibreria(filtro_nombre);
        this.llenarTabla(rs);
    }

    private void llenarTabla(ResultSet rs) throws SQLException {
        while (tbl_textos_prestamo.getRowCount() > 0) {
            ((DefaultTableModel) tbl_textos_prestamo.getModel()).removeRow(0);
        }
        int columns = rs.getMetaData().getColumnCount();
        while (rs.next()) {
            Object[] row = new Object[columns];
            for (int i = 1; i <= columns; i++) {
                row[i - 1] = rs.getObject(i);
            }
            ((DefaultTableModel) tbl_textos_prestamo.getModel()).insertRow(rs.getRow() - 1, row);
        }
    }

